import Foundation

func AlphabetSoup(_ str: String) -> String {
    return String(str.sorted())
}

AlphabetSoup("coderbyte") == "bcdeeorty"
AlphabetSoup("hooplah") == "ahhloop"
AlphabetSoup("love") == "elov"
AlphabetSoup("yoop") == "oopy"
AlphabetSoup("comma") == "acmmo"
